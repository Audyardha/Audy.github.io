package main

import "fmt"

func main() {
    // Penjumlahan
    var a = 10
    var b = 5
    var sum = a + b
    fmt.Println("Hasil penjumlahan:", sum)

    // Pengurangan
    var c = 20
    var d = 8
    var diff = c - d
    fmt.Println("Hasil pengurangan:", diff)

	//Perkalian
	var e = 12
	var f = 5
	var product = e * f
	fmt.Println("Hasil perkalian:", product)

	//Pembagian
	var g = 50
	var h = 10
	var quotient = g / h
	fmt.Println("Hasil pembagian:", quotient)
}


